# Helper to escape non-ASCII characters to standard C++ \uXXXX escapes
def to_cpp_unicode_literal(s):
    res = ""
    for c in s:
        code = ord(c)
        if code > 127:
            res += f"\\u{code:04x}"
        else:
            if c == '"':
                res += '\\"'
            elif c == '\\':
                res += '\\\\'
            elif c == '\n':
                res += '\\n'
            elif c == '\r':
                res += '\\r'
            elif c == '\t':
                res += '\\t'
            else:
                res += c
    return res


