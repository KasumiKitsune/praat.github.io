#include <mutex>
static std::unordered_map<std::u32string, std::u32string> g_dynamic_cache;
static std::mutex g_cache_mutex;

const char32* praat_translate (const char32* text) {
	if (!text) {
		return nullptr;
	}

	std::u32string key(text);
	auto it = g_translation_map.find(key);
	if (it != g_translation_map.end()) {
		return it->second.c_str();
	}

	{
		std::lock_guard<std::mutex> lock(g_cache_mutex);
		auto it_cache = g_dynamic_cache.find(key);
		if (it_cache != g_dynamic_cache.end()) {
			return it_cache->second.c_str();
		}
	}

	size_t leading_spaces = 0;
	while (leading_spaces < key.length() && key[leading_spaces] == U' ') {
		leading_spaces++;
	}

	if (leading_spaces > 0) {
		std::u32string stripped_key = key.substr(leading_spaces);
		auto it_stripped = g_translation_map.find(stripped_key);
		if (it_stripped != g_translation_map.end()) {
			std::u32string translated = key.substr(0, leading_spaces) + it_stripped->second;
			std::lock_guard<std::mutex> lock(g_cache_mutex);
			g_dynamic_cache[key] = translated;
			return g_dynamic_cache[key].c_str();
		}
	}

	return text;
}
